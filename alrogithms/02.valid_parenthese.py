def valid_parentheses(s: str) -> bool:
    stack = []
    pairs = {
        ')': '(',
        '}': '{',
        ']': '['
    }

    for c in s:
        if c in pairs:
            if not stack:
                return False
            last_c = stack.pop()
            if pairs[c] != last_c:
                return False
        else:
            stack.append(c)

    if len(stack) != 0:
        return False
    return True

